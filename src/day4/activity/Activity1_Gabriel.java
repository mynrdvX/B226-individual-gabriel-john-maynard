
package day4.activity;


public class Activity1_Gabriel {

    public static void main(String[] args) {

        // boolean - true/false value
        boolean isStudent = true;

        // byte - small integer (-128 to 127)
        byte myByte = 120;

        // short - larger integer
        short myShort = 20000;

        // int - standard integer
        int age = 26;

        // long - large integer (L at the end)
        long population = 800000000L;

        // float - decimal value (f at the end)
        float height = 5.9f;

        // double - more precise decimal
        double weight = 65.75;

        // char - single character
        char grade = 'A';

        // String - text
        String fullName = "John Maynard Gabriel";

        // Output with labels
        System.out.println("boolean: " + isStudent);
        System.out.println("byte: " + myByte);
        System.out.println("short: " + myShort);
        System.out.println("int: " + age);
        System.out.println("long: " + population);
        System.out.println("float: " + height);
        System.out.println("double: " + weight);
        System.out.println("char: " + grade);
        System.out.println("String: " + fullName);
    }
}